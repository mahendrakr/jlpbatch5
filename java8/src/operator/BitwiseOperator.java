package operator;

public class BitwiseOperator {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		if(a>5 & b>10) {
			
			System.out.println(true);
			}
		else {
			System.out.println(false);
		}
		System.out.println(a>5 & b>10);
		System.out.println(4 & 5);
		System.out.println(4 | 5);
		System.out.println(4^5);
	}

}
